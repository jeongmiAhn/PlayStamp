package com.playstamp.notice;

public class Notice
{
	private String notice_cd, mng_cd, title, contents, wr_dt, view_cnt;

	public String getNotice_cd()
	{
		return notice_cd;
	}

	public void setNotice_cd(String notice_cd)
	{
		this.notice_cd = notice_cd;
	}

	public String getMng_cd()
	{
		return mng_cd;
	}

	public void setMng_cd(String mng_cd)
	{
		this.mng_cd = mng_cd;
	}

	public String getTitle()
	{
		return title;
	}

	public void setTitle(String title)
	{
		this.title = title;
	}

	public String getContents()
	{
		return contents;
	}

	public void setContents(String contents)
	{
		this.contents = contents;
	}

	public String getWr_dt()
	{
		return wr_dt;
	}

	public void setWr_dt(String wr_dt)
	{
		this.wr_dt = wr_dt;
	}

	public String getView_cnt()
	{
		return view_cnt;
	}

	public void setView_cnt(String view_cnt)
	{
		this.view_cnt = view_cnt;
	}
	
	
}
